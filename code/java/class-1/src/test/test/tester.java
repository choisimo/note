package test;

public class tester{
    public static void main(String... args) {
        System.out.println("HELLO");
        System.out.println("Hello" + "HELLLLLLLLLL");
        System.out.println("2+4");
        System.out.println(2+4);
        System.out.println(5.5);
        System.out.println("HELLO" + 1 + 2);     // type-casting (숫자 -> 문자로 변경됨)
        System.out.println(1 + 2 + "HELLO");     // 위와 동일함
        System.out.println();
    }
}